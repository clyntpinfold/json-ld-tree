package daverog.jsonld.tree;

@SuppressWarnings("serial")
public class RdfTreeException extends Exception {

	public RdfTreeException() {
		super();
	}

	public RdfTreeException(String message, Throwable cause) {
		super(message, cause);
	}

	public RdfTreeException(String message) {
		super(message);
	}

	public RdfTreeException(Throwable cause) {
		super(cause);
	}
	
}
